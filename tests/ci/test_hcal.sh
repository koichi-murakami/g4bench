#!/bin/sh -
# ======================================================================
#  Build & run : hcal
# ======================================================================
export LANG=C

# ======================================================================
# functions
# ======================================================================
check_error() {
  if [ $? -ne 0 ]; then
    exit -1
  fi
}

show_line() {
echo "========================================================================"
}

# ======================================================================
# main
# ======================================================================
show_line
echo "@@ Build a program..."
cd build/hcal
make -j4
check_error

show_line
echo "@@ Run a program..."
export G4DATA=/opt/geant4/data/10.6.1
. ../../tests/g4env/g4env.10.6.1.sh

#
./hcal -j 2000

exit $?
