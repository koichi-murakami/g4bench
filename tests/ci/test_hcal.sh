#!/bin/sh -
# ======================================================================
#  Build & run : hcal
# ======================================================================
export LANG=C

# ======================================================================
# functions
# ======================================================================
check_error() {
  if [ $? -ne 0 ]; then
    exit -1
  fi
}

show_line() {
echo "========================================================================"
}

# ======================================================================
# main
# ======================================================================
show_line
echo "@@ Configure a program..."
./configure --with-geant4-dir=/opt/geant4/${G4VERSION} --disable-vis

show_line
echo "@@ Build a program..."
cd build/hcal
make -j4
check_error

show_line
echo "@@ Run a program..."
./hcal -j 2000

exit $?
